package tiwulfx.samples.shared.pojo;

import javax.persistence.*;
import java.io.Serializable;
import java.util.List;

/**
 * @author Amrullah <amrullah@panemu.com>
 */
@Entity
@Table(name = "insurance")
@NamedQueries({@NamedQuery(name = "Insurance.findAll", query = "SELECT i FROM Insurance i")})
public class Insurance implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @Column(name = "code")
    private String code;
    @Basic(optional = false)
    @Column(name = "name")
    private String name;
    @OneToMany(mappedBy = "insurance", fetch = FetchType.LAZY)
    private List<Person> personList;

    public Insurance() {
    }

    public Insurance(Integer id) {
        this.id = id;
    }

    public Insurance(Integer id, String code, String name) {
        this.id = id;
        this.code = code;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Person> getPersonList() {
        return personList;
    }

    public void setPersonList(List<Person> personList) {
        this.personList = personList;
    }


    @Override
    public String toString() {
        return "com.panemu.tiwulfx.demo.pojo.Insurance[ id=" + id + " ]";
    }

}
